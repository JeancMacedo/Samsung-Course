print('Informe dois números e veja qual é o menor e o maior!')
n1 = int(input('Digite o primeiro número: '))
n2 = int(input('Digite o segundo número: '))

if n1 > n2:
    print(f'Os número inteiros em ordem crescente são: {n1} {n2}')
else:
    print(f'Os número inteiros em ordem crescente são: {n2} {n1}')
