# Definindo o dicionário maria
maria = {'coreano': 94, 'ingles': 91, 'matematica': 89, 'ciencia': 83}

# Calculando a soma das notas
soma_notas = sum(maria.values())

# Calculando o número de matérias
numero_materias = len(maria)

# Calculando a média das notas
media_notas = soma_notas / numero_materias

# Imprimindo a média das notas
print(media_notas)