#index( x )	Encontra um lugar usando o elemento x.
#append( x )	Acrescenta o elemento x ao final da lista.
#count( x )	Retorna o número de elementos de objeto dentro da lista.
#extend([x1, x2])	Insere a lista [x1, x2] na lista.
#insert(index, x)	Adiciona o x ao local do índice desejado.
#remove( x )	Elimina o elemento x da lista.
#pop( index )	Deleta o elemento no local do índice e depois o devolve. Se for o caso, o índice pode ser omitido, o que apagará e retornará o último elemento da lista. 
#sort()	Ordena os valores em ordem ascendente. Se o valor do argumento inverso for Verdadeiro, a ordenação por ordem decrescente é feita.
#reverse()	Faz uma ordem inversa dos elementos da lista.