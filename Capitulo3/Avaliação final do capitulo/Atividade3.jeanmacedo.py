# Lista original
n_list = [10, 20, 30]

# Multiplicando os valores por 2 usando a função map
multiplicados_por_2 = list(map(lambda x: x * 2, n_list))

# Multiplicando os valores por 3 usando a função map
multiplicados_por_3 = list(map(lambda x: x * 3, n_list))

# Imprimindo os resultados
print("Valores multiplicados por 2:", multiplicados_por_2)
print("Valores multiplicados por 3:", multiplicados_por_3)
